<template>
    <div class="login-wrap">
        <div class="ms-login">
            <div class="ms-title">车位宝后台管理系统</div>
            <el-form :model="ruleForm" :rules="rules" ref="ruleForm" label-width="0px" class="ms-content">
                <el-form-item prop="account">
                    <el-input v-model="ruleForm.account" placeholder="请输入账号">
                        <el-button slot="prepend" icon="el-icon-lx-people"></el-button>
                    </el-input>
                </el-form-item>
                <el-form-item prop="password">
                    <el-input type="password" placeholder="请输入密码" v-model="ruleForm.password" @keyup.enter.native="submitForm('ruleForm')">
                        <el-button slot="prepend" icon="el-icon-lx-lock"></el-button>
                    </el-input>
                </el-form-item>
                <div class="login-btn">
                    <el-button type="primary" @click="submitForm('ruleForm')">登录</el-button>
                </div>
                
            </el-form>
        </div>
    </div>
</template>

<script>
    export default {
        data: function(){
            return {
                ruleForm: {
                    account: '',
                    password: ''
                },
                rules: {
                    account: [
                        { required: true, message: '请输入用户名', trigger: 'blur' }
                    ],
                    password: [
                        { required: true, message: '请输入密码', trigger: 'blur' }
                    ]
                }
            }
        },
        methods: {
            submitForm() {
                var params = new URLSearchParams();
				params.append('account', this.ruleForm.account);
				params.append('password', this.ruleForm.password);
				this.$axios.post(request.testUrl+"/login",params)
					.then(res=>{
						//console.log(res)
						if(res.data.code==0){
							var name=res.data.data.name
							console.log(res)
							this.$router.push({ path: '/',query:{'name':name}}) 
						}else{
							this.$message({
								type: 'info',
								message: res.data.msg
							});	
						}		
	        		}).catch(res=>{
	        			this.$message({
								type: 'info',
								message: res.data.msg
							});	
	        		})
            }
        }
    }
</script>

